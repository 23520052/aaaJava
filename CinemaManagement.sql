-- Thêm chỉ mục để tối ưu
-- Bảng PHIM: Tìm kiếm phim theo tên
CREATE INDEX idx_phim_ten ON PHIM(TEN_PHIM);

-- Bảng SUAT_CHIEU: Tìm suất chiếu theo ngày và giờ
CREATE INDEX idx_suatchieu_ngaygio ON SUAT_CHIEU(NGAY_CHIEU, GIO_CHIEU);

